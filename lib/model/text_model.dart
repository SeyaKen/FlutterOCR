import 'package:google_ml_kit/google_ml_kit.dart';

class RecognizedText {
  // プロパティ
  // => プロパティとは、単にクラスの中で使う変数のことです。
  // よってプロパティは変数と同じようにクラスで宣言します。
  String? block;

  // コンストラクタ
  // => コンストラクタは、クラスのオブジェクトが
  // インスタンス化される時に一度だけ実行されます。
  // また、パラメータを使えば、クラスに引数を渡すことができます。
  RecognizedText({this.block});

  // メソッドとは、単にクラスの中で使う関数のことです。
  // よってメソッドは関数と同じようにクラスで定義します。
}
